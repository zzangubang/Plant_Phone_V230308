package com.example.plant_iot_phone2;

public class BluetoothActivity_ListItem {
    String name, address;

    public BluetoothActivity_ListItem(String name, String address) {
        this.name = name;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
